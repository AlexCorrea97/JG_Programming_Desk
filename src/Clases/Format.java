package Clases;

/**
 *
 * @author Alejandro Correa
 */
public class Format {
    private int id;
    private String format;

    public Format(int id, String format) {
        this.id = id;
        this.format = format;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getFormat() {
        return format;
    }

    public void setFormat(String format) {
        this.format = format;
    }
    
    
}
