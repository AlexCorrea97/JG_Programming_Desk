    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(!txtName.getText().equals("")){
        Http http = new Http();
        String body = http.GET(URLs.GetEjercicios);
        Gson gson = new Gson();
        java.lang.reflect.Type tipoListaEjercicio = new TypeToken<List<Ejercicio>>() {
        }.getType();
        ejerciciosList = gson.fromJson(body, tipoListaEjercicio);
        int id = 0;
        int size = ejerciciosList.size();
        boolean flag = true;
        for (int i = 1; i <= size; i++) {
            if (i != ejerciciosList.get(i - 1).getId()) {
                id = i;
                flag = false;
                break;
            }
        }
        if (flag) {
            id = size + 1;
        }

        Ejercicio ej = new Ejercicio(id, txtName.getText().toUpperCase(), txtLink.getText());
        http=new Http();
        try{
        String str=http.GET(URLs.InsertEjercicio(ej));
        if(str.equals("Ejercicio agregado correctamente")){
        JOptionPane.showMessageDialog(this, "Ejercicio agregado", "Bien", JOptionPane.INFORMATION_MESSAGE);
        this.setVisible(false);
        }
        else{
        JOptionPane.showMessageDialog(this,str , "Error", JOptionPane.ERROR_MESSAGE);
        }
        }
        catch(Exception ex){
        JOptionPane.showMessageDialog(this, ex.toString(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        }else{
        JOptionPane.showMessageDialog(this, "El ejercicio debe tener un nombre vÃ¡lido", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

